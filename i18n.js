let currentLang = (typeof localStorage !== 'undefined' && localStorage.getItem(STORAGE_KEY)) || 'en';
if (!TRANSLATIONS[currentLang]) currentLang = 'en';

/**
 * Get translated string for key. Falls back to English then to key.
 */
export function t(key) {
    const tr = TRANSLATIONS[currentLang];
    const en = TRANSLATIONS.en;
    if (tr && tr[key] !== undefined) return tr[key];
    if (en && en[key] !== undefined) return en[key];
    return key;
}

/**
 * Get current language code.
 */
export function getLang() {
    return currentLang;
}

/**
 * Set language, persist, and re-apply all translations. Dispatches 'langchange' event.
 */
export function setLang(code) {
    if (!TRANSLATIONS[code]) return;
    currentLang = code;
    try {
        localStorage.setItem(STORAGE_KEY, code);
    } catch (_) {}
    document.documentElement.lang = code === 'zh' ? 'zh' : code === 'br' ? 'br' : code;
    applyTranslations();
    window.dispatchEvent(new CustomEvent('langchange', { detail: { lang: code } }));
}

/**
 * Apply translations to DOM: [data-i18n], [data-i18n-placeholder], [data-i18n-title].
 */
export function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (key) el.textContent = t(key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (key) el.placeholder = t(key);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        if (key) el.title = t(key);
    });
    // Page title
    const titleEl = document.querySelector('title');
    if (titleEl) titleEl.textContent = t('title');
}
